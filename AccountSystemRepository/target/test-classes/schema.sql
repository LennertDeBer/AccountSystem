CREATE SCHEMA LENNERT;

CREATE SEQUENCE LENNERT.AC_TYPE_GENERIC_SEQ START WITH 50002 INCREMENT BY 1;
CREATE SEQUENCE LENNERT.AC_TX_GENERIC_SEQ START WITH 1 INCREMENT BY 1;

CREATE table LENNERT.ACCOUNT_TYPE
(
ACCOUNT_TYPE_ID NUMBER(15) not null,
MNEMONIC VARCHAR2(50) not null,
ACCOUNT_TYPE_NAME VARCHAR2(50) NOT NULL,
CREATION_DATE DATE NOT NULL,
constraint pk_ACCOUNT_TYPE_ID primary key (ACCOUNT_TYPE_ID)
);

CREATE TABLE LENNERT.ACCOUNT_TX
(
TX_ID NUMBER(15)   not null,
ACCOUNT_TYPE_ID NUMBER(15) not null,
MEMBER_ID NUMBER(15) NOT NULL,
AMOUNT NUMBER(15) not null,
TX_DATE DATE NOT NULL,
constraint pk_TX_ID primary key (TX_ID),
CONSTRAINT fk_ACCOUNT_TYPE_ID FOREIGN KEY (ACCOUNT_TYPE_ID)REFERENCES LENNERT.ACCOUNT_TYPE(ACCOUNT_TYPE_ID)
);
